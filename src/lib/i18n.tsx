type Translations = typeof TRANSLATIONS.en;
type DotKeys<T, P extends string = ''> = {
  [K in keyof T & string]: T[K] extends object ? DotKeys<T[K], `${P}${K}.`> : `${P}${K}`;
}[keyof T & string];
export type TKey = DotKeys<Translations>;

interface I18nApi {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TKey, vars?: Record<string, string | number>) => string;
  isRTL: boolean;
}

const I18nCtx = createContext<I18nApi | null>(null);

function lookup(table: unknown, parts: string[]): string | null {
  let cur: unknown = table;
  for (const p of parts) {
    if (cur && typeof cur === 'object' && p in (cur as Record<string, unknown>)) {
      cur = (cur as Record<string, unknown>)[p];
    } else return null;
  }
  return typeof cur === 'string' ? cur : null;
}

function applyVars(s: string, vars?: Record<string, string | number>): string {
  if (!vars) return s;
  return s.replace(/\{(\w+)\}/g, (_, k) => (k in vars ? String(vars[k]) : `{${k}}`));
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const v = window.localStorage.getItem(STORAGE_KEY) as Lang | null;
    return v && (v === 'en' || v === 'ar' || v === 'fr') ? v : 'en';
  });

  // Apply <html lang + dir> live so Tailwind RTL utilities + browser
  // bidi engine kick in immediately.
  useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = RTL_LANGS.has(lang) ? 'rtl' : 'ltr';
  }, [lang]);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    window.localStorage.setItem(STORAGE_KEY, l);
  }, []);

  const t = useCallback(
    (key: TKey, vars?: Record<string, string | number>): string => {
      const parts = key.split('.');
      // Try selected language first; fall back to English on missing keys
      // so the UI never renders the raw key.
      const hit = lookup(TRANSLATIONS[lang], parts) ?? lookup(TRANSLATIONS.en, parts);
      if (hit == null) return key;
      return applyVars(hit, vars);
    },
    [lang],
  );

  const api = useMemo<I18nApi>(
    () => ({ lang, setLang, t, isRTL: RTL_LANGS.has(lang) }),
    [lang, setLang, t],
  );

  return <I18nCtx.Provider value={api}>{children}</I18nCtx.Provider>;
}

export function useI18n(): I18nApi {
  const v = useContext(I18nCtx);
  if (!v) throw new Error('useI18n must be used inside <I18nProvider>');
  return v;
}

export function useT() {
  return useI18n().t;
}
